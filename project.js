// Get project ID from URL
function getProjectId() {
  const urlParams = new URLSearchParams(window.location.search);
  return urlParams.get('id') || '1';
}

// Load project data
function loadProject() {
  const projectId = getProjectId();
  const project = projectsData[projectId];

  if (!project) {
    window.location.href = 'index.html';
    return;
  }

  // Update page title
  document.title = `${project.title} | Portfolio`;

  // Update hero section
  document.getElementById('project-title').textContent = project.title;
  document.getElementById('project-tagline').textContent = project.tagline;
  document.getElementById('project-role').textContent = project.role;
  document.getElementById('project-year').textContent = project.year;
  document.getElementById('project-client').textContent = project.client;

  // Update cover image
  const coverImage = document.getElementById('cover-image');
  coverImage.innerHTML = `<img src="${project.coverImage}" alt="${project.title}">`;

  // Update content sections
  document.getElementById('project-overview').innerHTML = project.overview.map(p => `<p>${p}</p>`).join('');
  document.getElementById('project-challenge').innerHTML = project.challenge.map(p => `<p>${p}</p>`).join('');
  document.getElementById('project-solution').innerHTML = project.solution.map(p => `<p>${p}</p>`).join('');

  // Update gallery
  const galleryGrid = document.getElementById('gallery-grid');
  galleryGrid.innerHTML = project.gallery.map(item => `
    <div class="gallery-item ${item.fullWidth ? 'full-width' : ''}">
      <img src="${item.src}" alt="${project.title}">
    </div>
  `).join('');

  // Update results
  const resultsGrid = document.querySelector('.results-grid');
  resultsGrid.innerHTML = project.results.map(result => `
    <div class="result-item">
      <span class="result-number">${result.number}</span>
      <span class="result-label">${result.label}</span>
    </div>
  `).join('');

  // Update technologies
  const techList = document.getElementById('tech-list');
  techList.innerHTML = project.technologies.map(tech => `<span class="tech-tag">${tech}</span>`).join('');

  // Update testimonial
  const testimonialQuote = document.querySelector('.testimonial-quote');
  const authorName = document.querySelector('.author-name');
  const authorRole = document.querySelector('.author-role');
  testimonialQuote.textContent = `"${project.testimonial.quote}"`;
  authorName.textContent = project.testimonial.author;
  authorRole.textContent = project.testimonial.role;

  // Update next project
  document.getElementById('next-project-link').href = `project.html?id=${project.nextProject.id}`;
  document.getElementById('next-project-title').textContent = project.nextProject.title;
}

// Mobile menu toggle
function initMobileMenu() {
  const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
  const navLinks = document.querySelector('.nav-links');

  if (mobileMenuBtn && navLinks) {
    mobileMenuBtn.addEventListener('click', () => {
      navLinks.classList.toggle('active');
    });
  }
}

// Set current year
function setCurrentYear() {
  const yearElement = document.getElementById('year');
  if (yearElement) {
    yearElement.textContent = new Date().getFullYear();
  }
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
  loadProject();
  initMobileMenu();
  setCurrentYear();
});

